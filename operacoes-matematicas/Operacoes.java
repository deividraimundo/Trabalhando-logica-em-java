
public class Operacoes {
    
    Double soma (double n1, double n2){
    return n1 + n2;
}
    Double subtracao (double n1, double n2){
    return n1 - n2;
}
    Double multiplicar (double n1, double n2){
    return n1 * n2;
}
    Double dividir (double n1, double n2){
    return n1 / n2;
}
    Double resto (double n1, double n2){
    return n1 % n2;
}
}   

